import { angleBetweenVectors } from './angle-between-vectors';

describe('angleBetweenVectors', () => {
  test.each`
    x1      | y1      | x2      | y2      | expected
    ${100}  | ${100}  | ${1000} | ${1000} | ${1.4901161193847656e-8}
    ${100}  | ${100}  | ${101}  | ${100}  | ${0.004975083330842005}
    ${100}  | ${100}  | ${100}  | ${101}  | ${0.004975083330842005}
    ${100}  | ${200}  | ${200}  | ${100}  | ${0.6435011087932843}
    ${200}  | ${100}  | ${100}  | ${200}  | ${0.6435011087932843}
    ${2000} | ${100}  | ${200}  | ${200}  | ${0.7354397676755057}
    ${100}  | ${2000} | ${200}  | ${200}  | ${0.7354397676755057}
    ${100}  | ${2000} | ${100}  | ${2001} | ${0.000024925220034288298}
    ${0}    | ${1}    | ${1}    | ${0}    | ${1.5707963267948966}
    ${1}    | ${0}    | ${0}    | ${1}    | ${1.5707963267948966}
    ${58}   | ${58}   | ${57}   | ${57}   | ${0}
  `('angleBetweenVectors($x1, $y1, $x2, $y2) [$expected]', (testCase) => {
    const { x1, y1, x2, y2, expected } = testCase;
    const results = angleBetweenVectors(x1, y1, x2, y2);
    expect(results).toBeCloseTo(expected);
  });
});
